arregloEnteros = [1,4,2,3,6,5,7,8]
print(arregloEnteros)

#agregar nuevo elemento
arregloEnteros.append(-5)
print(arregloEnteros)

#quitar elemento
arregloEnteros.remove(3)
print(arregloEnteros)

#revertir el arreglo
arregloEnteros.reverse()
print(arregloEnteros)

#ordenar los elementos del arreglo
arregloEnteros.sort()
print(arregloEnteros)